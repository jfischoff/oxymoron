    leqSymbol :: Symbol -> Symbol -> Bool
    leqSymbol (Symbol [])     (Symbol _)      = False
    leqSymbol (Symbol (x:_))  (Symbol [])     = True 
    leqSymbol (Symbol (x:xs)) (Symbol (y:ys)) = proj (leqAChar x y) 
        (leqSymbol (Symbol xs) (Symbol ys))
    
