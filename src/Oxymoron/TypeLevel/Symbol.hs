    insert :: Symbol -> [Symbol] -> [Symbol]
    insert n [] = [n]
    insert n (h:t) = if leqSymbol n h then (n:h:t) else h:(insert n t)

    insertionSort :: [Symbol] -> [Symbol]
    insertionSort [] = []
    insertionSort (h:t) = insert h (insertionSort t)
    
    
